    @staticmethod
    def grid_dimensions(grid) -> tuple[int, int]:
        """Return (rows, cols) for a level grid."""
        if not grid:
            return Maps.REFERENCE_GRID_SIZE, Maps.REFERENCE_GRID_SIZE
        rows = len(grid)
        cols = max((len(row) for row in grid), default=rows)
        return rows, cols

    def level_grid_size(self, level_id: int) -> int:
        """Largest side length for a level (used for uniform scaling)."""
        rows, cols = self.grid_dimensions(self.levels[level_id])
        return max(rows, cols)

    def validate_level(self, level_id: int) -> bool:
        """Every row must have the same width."""
        grid = self.levels.get(level_id)
        if not grid:
            return False
        width = len(grid[0])
        return all(len(row) == width for row in grid)

    @staticmethod
    def level_for_wave(wave: int) -> int:
        """Wave N uses sunshine layout 1-20 (same 10 maps as pre-cave waves 11-20)."""
        return max(1, min(int(wave), 20))

    @staticmethod
    def stage_for_wave(wave: int) -> str:
        """All waves use the sunshine stage."""
        return "sunshine"

    @staticmethod
    def branches_for_wave(wave: int) -> int:
        """Branch budget to buy mobs during combat (see main.WAVE_BRANCHES)."""
        from main import branches_for_wave as _branches_for_wave
        return _branches_for_wave(wave)

    @staticmethod
    def tree_health_for_wave(wave: int) -> int:
        """Tree HP at wave start (see main.WAVE_TREE_HEALTH)."""
        from main import tree_health_for_wave as _tree_health_for_wave
        return _tree_health_for_wave(wave)
